#include "../pipex.h"

int	**free_tabs(int **tabs)
{
	int	i;

	i = 0;
	while (tabs[i])
	{
		free(tabs[i]);
		tabs[i++] = NULL;
	}
	free(tabs);
	return (NULL);
}
