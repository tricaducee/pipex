#include "../HEADER/pipex.h"

void	if_close_fd(int i, t_ptr *tabs)
{
	if (i)
	{
		close(tabs->fd[i][0]);
		close(tabs->fd[i][1]);
	}
	else
		close(tabs->fd[i][0]);
}
